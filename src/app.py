"""
Помощник эксперта по сертификации
Приложение для подбора стандартов на основе базы выданных сертификатов
"""

import streamlit as st

# Настройка страницы
st.set_page_config(
    page_title="Помощник эксперта по сертификации",
    page_icon="📋",
    layout="wide"
)


def main():
    st.title("📋 Помощник эксперта по сертификации")
    st.markdown("### Подбор стандартов для испытаний продукции")

    st.info("👋 Приложение в разработке. Сейчас идёт настройка структуры проекта.")

    # Тестовое поле ввода
    product_query = st.text_input(
        "Введите описание продукции:",
        placeholder="Например: пылесос бытовой"
    )

    if product_query:
        st.write(f"Поиск стандартов для: **{product_query}**")
        st.warning("⚠️ База данных ещё не загружена. Ждите следующего этапа разработки.")


if __name__ == "__main__":
    main()